package datatype;

public class demo2 {
    public static void main(String[] args) {
        int int_1 = 0b1010;
        int int_2 = 012;
        int int_3 = 10;
        int int_4 = 0xA;
        System.out.println(int_1);
        System.out.println(int_2);
        System.out.println(int_3);
        System.out.println(int_4);
        byte byte_1;
        byte_1 = (byte) int_1;
        System.out.println(byte_1);
        System.out.println("5+5="+5+5);//55
        System.out.println("5+5="+(5+5));//10
    }
}
